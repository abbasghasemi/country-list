    /**
     * @param bool $waving
     * @param int $size
     * @param string $extension
     * @return string url
     * @link https://flagpedia.net/download/api
     */
    public function getFlagUrl(bool $waving = false, int $size = self::SIZE_20, string $extension = self::EXT_SVG): string
    {
        $baseUrl = "https://flagcdn.com/";
        if ($extension === self::EXT_SVG) {
            return "{$baseUrl}{$this->value}.{$extension}";
        }
        if ($waving && in_array($extension, [self::EXT_PNG, self::EXT_WEBP])) {
            $xSize = match ($size) {
                self::SIZE_20 => '20x15',
                self::SIZE_40 => '40x30',
                self::SIZE_80 => '80x60',
                self::SIZE_160 => '160x120',
                self::SIZE_320, self::SIZE_640, self::SIZE_1280, self::SIZE_2560 => '256x192',
                default => "16x12"
            };
            return "{$baseUrl}{$xSize}/{$this->value}.{$extension}";
        }
        return "{$baseUrl}w{$size}/{$this->value}.{$extension}";
    }

